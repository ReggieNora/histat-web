 <p class="normal">

All together, all in the database Histat contained time series enclose about five and a half million values. From the year 1800 at least thousand values for nearly each year are available, from the middle of the 19th century there are already more than 10.000 values on hand via Histat, and with the foundation of the German Empire (in 1871) there are more than 20.000 values available per year. The highlights are the years at the beginning of the 20th Century: for several years there are about 60.000 values available via histat. But in particular for the forties of the 20th century the number of available values drops to less than 2.000. For the post-war period up to the present there are between 20.000 and 30.000 values in histat. In comparison with that the so-called prestatistical period (i.e. the period prior to 1871) is rather modest. However, also here we can offer since the 16th century predominantly more than hundred values per year, and since the mid of the 17th century at least for every tenth year more than thousand values.</p>
