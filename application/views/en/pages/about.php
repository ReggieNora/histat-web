            <div id="welcome" style="border:0px"><h1>About histat</h1><br/>
                <h3>Database on historical statistics</h3>
                <p class="normal">
                    Via the database-system histat we offer since 2004 a product, which was developed and promoted by the GESIS-staff-members Jürgen Sensch, Gabriele Franzmann, and Rainer Metz. Histat serves as access-platform of time series data collected in the framework of economic and social historical research. Two important purposes were to be served by the development of a data-access-platform: On the one hand the access of the data stock should be uncomplicated and therefore user-friendly, on the other hand the database should meet scientific standards and offer time-series together with a detailed documentation of their context (as well as annotations on data-sources). These objectives have been successfully implemented via the histat-database. This year we will attain more than 3000 registered user from the whole world. The data base encloses meanwhile about a quarter of million time series from more than 360 studies. During the last year more than 70.000 time series has been downloaded. Monthly several studies are imported into the database. In addition histat has got an organizational solid basis. The closing of the Center of Historical Social Research (Zentrum für Historische Sozialforschung, ZHSF) and the implementation of a new team structure within GESIS ensures that the Histat-database is supervised and developed permanently by the team  <a href="http://www.gesis.org/unser-angebot/daten-analysieren/daten-historischer-studien/">Data service Historical Studies</a>. The team currently consists of ten employees. Three employees of this team are dedicated to the continuous care and expansion of histat’s data stock. In doing so they are supported by student assistants. In 2011 GESIS provided funds for an extensive relaunch. With these financial means the surface has been reworked basically and a consistent user interface across all functions was created. 
                 </p>
