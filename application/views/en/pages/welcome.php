Welcome to the online-database <span class="orange">histat</span>.
    </p>
<p class="normal">
    Histat was developed in GESIS by Jürgen Sensch, Rainer Metz, and Gabriele Franzmann and released in 2004. The online platform has the function of making time series from historical, economic and social science research accessible. New studies are frequently imported into histat.
<br>
    The registration, the use of histat as well as the download of data from histat are free of charge.
</p>

<h3>Citation</h3>
<p class="normal">
    Please cite the source of the data used and the publications to the data in your own publications according to scientific practice. Recommendations for citation of historical data can be found under <?= HTML::anchor('https://www.gesis.org/angebot/daten-analysieren/weitere-sekundaerdaten/daten-historischer-studien/datenbank-histat/', '"Data Quotation"', array('target' => 'blank')) ?>.</p>
<p class="normal">
    HISTAT offers extensive "meta-information" for each study: in addition to a detailed study description, it also provides detailed information on the sources used in the study, the study area, and other comments.
    <br>
    All metadata are available via a comfortable search function free of charge and without registration. Since we have received the data from our data providers under certain conditions for dissemination, we are obliged to prove the use of this data using the registration.
</p>
